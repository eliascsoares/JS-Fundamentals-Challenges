let name = "Alice";
let age = 30;
let favouriteColour = "blue";

function logPersonInfo(name, age, colour) {
    console.log(`${name} is ${age} years old and their favourite colour is ${colour}.`);
}

logPersonInfo(name, age, favouriteColour);

name = "Bob";
age = 25;
favouriteColour = "green";

logPersonInfo(name, age, favouriteColour);